public class Character
{
  private String name;
  private int level;
  private long exp;
  private int hp;
  private int mp;
  private int str;
  private int def;
  private int mdef;

  public String getName()
  {
    return name;
  }
  public void setName(String tmpName)
  {
    name = tmpName;
  }

  public int getLevel()
  {
    return level;
  }
  public void setLevel(int tmpLevel)
  {
    level = tmpLevel;
  }

  public long getEXP()
  {
    return exp;
  }
  public void setEXP(long tmpEXP)
  {
    exp = tmpEXP;
  }

  public int getHP()
  {
    return hp;
  }
  public void setHP(int tmpHP)
  {
    hp = tmpHP;
  }

  public int getMP()
  {
    return mp;
  }
  public void setMP(int tmpMP)
  {
    mp = tmpMP;
  }

  public int getSTR()
  {
    return str;
  }
  public void setSTR(int tmpSTR)
  {
    str = tmpSTR;
  }

  public int getDEF()
  {
    return def;
  }
  public void setDEF(int tmpDEF)
  {
    def = tmpDEF;
  }

  public int getMDEF()
  {
    return mdef;
  }
  public void setMDEF(int tmpMDEF)
  {
    mdef = tmpMDEF;
  }
}
