public class Enemy
{
  private int exp;
  private int hp;
  private int mp;
  private int str;
  private int def;
  private int mdef;

  public int getEXP()
  {
    return exp;
  }
  public void setEXP(int tmpEXP)
  {
    exp = tmpEXP;
  }

  public int getHP()
  {
    return hp;
  }
  public void setHP(int tmpHP)
  {
    hp = tmpHP;
  }
  public int getMP()
  {
    return mp;
  }
  public void setMP(int tmpMP)
  {
    mp = tmpMP;
  }

  public int getSTR()
  {
    return str;
  }
  public void setSTR(int tmpSTR)
  {
    str = tmpSTR;
  }

  public int getDEF()
  {
    return def;
  }
  public void setDEF(int tmpDEF)
  {
    def = tmpDEF;
  }

  public int getMDEF()
  {
    return mdef;
  }
  public void setMDEF(int tmpMDEF)
  {
    mdef = tmpMDEF;
  }
}
